int is_palin (int val)
{
    static char buf[20];
    int i, j;

    sprintf (buf, "%d", val);
    j = strlen (buf)-1;
    for (i = 0; i < j; i++, j--) {
        if (buf[i] != buf[j])
            return 0;
    }

    return 1;
}



void build_primes (int max)
{
    int val, i;
    int prime;

    primes[0] = 2;
    primes_count = 1;

    for (val = 3; val < max; val += 2) {
        prime = 1;

        for (i = 0; i < primes_count && prime; i++) {
            if (val % primes[i] == 0)
                prime = 0;
            if (primes[i] * primes[i] > val)
                break;
        }

        if (prime && is_palin (val))
            primes[primes_count++] = val;

        if (prime && val > 1000 && val < 10000)
            printf ("%d\n", val);
    }
}



